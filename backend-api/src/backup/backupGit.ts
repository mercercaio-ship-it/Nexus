/** Returns true iff `repoDir/.git/` exists. Read-only; never modifies state. */
export async function isBackupRepoInitialized(repoDir: string): Promise<boolean> {
  try {
    const s = await stat(join(repoDir, ".git"));
    return s.isDirectory();
  } catch {
    return false;
  }
}

/** Returns true iff `repoDir` has an `origin` remote configured. */
export async function hasOriginRemote(repoDir: string): Promise<boolean> {
  try {
    const r = await runGit(["remote"], repoDir);
    return r.stdout
      .split("\n")
      .map((s) => s.trim())
      .includes("origin");
  } catch {
    return false;
  }
}
