/**
 * Phase 5.6-A - opt-in backup git helpers + copy strategy.
 *
 * Everything in this module is conservative by design:
 *
 *   - `git` is invoked via `execFile` with `shell:false` and array args.
 *     No string interpolation into a shell. No environment leakage beyond
 *     `PATH`. Bounded timeout (15 s per call). Captured stdout/stderr are
 *     hard-capped at `MAX_OUTPUT_BYTES` so an unusually chatty git can't
 *     blow our event loop.
 *
 *   - The copy step is restricted to `agents/<slug>/memory/*.md`. The
 *     allowlist is on the destination side too: even if the source dir
 *     somehow contained sessions.db or a provider config, we'd refuse to
 *     write it into the backup repo. The `.gitignore` generator is a
 *     belt-and-braces second layer.
 *
 *   - `pushBackup` is only invoked when the caller explicitly sets
 *     `push:true` AND a remote is configured. There is no implicit push,
 *     no daemon, no schedule. The user is the trigger.
 *
 *   - Remote URLs are redacted before logging or echoing back to HTTP.
 *     A URL like `https://user:tok@github.com/...` is rewritten to
 *     `https://***@github.com/...` so the response can't expose a token
 *     the user accidentally embedded.
 */

import { execFile } from "node:child_process";
import { promisify } from "node:util";
import {
  mkdir,
  readdir,
  readFile,
  stat,
  writeFile,
} from "node:fs/promises";
import { dirname, join, relative, resolve, sep } from "node:path";

const execFileP = promisify(execFile);

const GIT_TIMEOUT_MS = 15_000;
const MAX_OUTPUT_BYTES = 1024 * 256; // 256 KB
const MAX_FILES_PER_RUN = 5000;
const MAX_FILE_BYTES = 4 * 1024 * 1024; // 4 MB per file

export class BackupGitError extends Error {
  readonly code:
    | "git_missing"
    | "git_failed"
    | "io_error"
    | "path_escapes_repo"
    | "too_many_files";
  constructor(code: BackupGitError["code"], message: string) {
    super(message);
    this.name = "BackupGitError";
    this.code = code;
  }
}

