export interface StatusSummary {
  added: number;
  modified: number;
  deleted: number;
  untracked: number;
  /** True iff at least one of added/modified/deleted/untracked is > 0. */
  changed: boolean;
}

/**
 * Parse `git status --porcelain=v1 -z` output into a tiny summary. The
 * `-z` form NUL-separates entries so filenames with whitespace or
 * newlines can't confuse the parser.
 *
 * Status codes (see git-status(1)):
 *   "M " / " M" / "MM" -> modified
 *   "A " / " A"        -> added
 *   "D " / " D"        -> deleted
 *   "??"               -> untracked
 *   "R "               -> renamed (counted as modified)
 *   anything else      -> ignored
 */
export function parseStatusPorcelain(raw: string): StatusSummary {
  let added = 0,
    modified = 0,
    deleted = 0,
    untracked = 0;
  if (!raw) {
    return { added, modified, deleted, untracked, changed: false };
  }
  const entries = raw.split("\0").filter((s) => s.length > 0);
  for (const entry of entries) {
    if (entry.length < 3) continue;
    const code = entry.slice(0, 2);
    if (code === "??") {
      untracked++;
    } else if (code.includes("A")) {
      added++;
    } else if (code.includes("D")) {
      deleted++;
    } else if (code.includes("M") || code.includes("R")) {
      modified++;
    }
  }
  return {
    added,
    modified,
    deleted,
    untracked,
    changed: added + modified + deleted + untracked > 0,
  };
}

export async function getGitStatus(repoDir: string): Promise<StatusSummary> {
  const r = await runGit(["status", "--porcelain=v1", "-z"], repoDir);
  if (r.exitCode !== 0) {
    throw new BackupGitError(
      "git_failed",
      "git status failed: " + (r.stderr || "unknown").slice(0, 400)
    );
  }
  return parseStatusPorcelain(r.stdout);
}

export interface CommitResult {
  committed: boolean;
  hash: string | null;
  message: string;
}

/**
 * Stage the `agents/` subtree + the `.gitignore`, then commit if there is
 * anything to commit. The two file scopes are explicit on purpose so the
 * commit can never accidentally pick up an unexpected sibling file that
 * appeared in the repo dir.
 *
 * Returns `{committed:false, hash:null}` when the working tree is clean.
 */
export async function commitBackup(
  repoDir: string,
  message: string
): Promise<CommitResult> {
  // Stage only the paths we control. Each pathspec is added independently
  // so a missing `agents/` directory (e.g. a fresh repo with just the
  // .gitignore) doesn't fail the whole commit. `git add` exits non-zero
  // when the pathspec doesn't match anything, so we check existence first.
  const pathsToStage: string[] = [];
  try {
    await stat(join(repoDir, "agents"));
    pathsToStage.push("agents");
  } catch {
    // agents/ doesn't exist yet; nothing to stage on this path.
  }
  try {
    await stat(join(repoDir, ".gitignore"));
    pathsToStage.push(".gitignore");
  } catch {
    // No .gitignore yet (unusual but not fatal).
  }
  if (pathsToStage.length > 0) {
    const stageResult = await runGit(["add", "--", ...pathsToStage], repoDir);
    if (stageResult.exitCode !== 0) {
      throw new BackupGitError(
        "git_failed",
        "git add failed: " + (stageResult.stderr || "unknown").slice(0, 400)
      );
    }
  }
  // Anything to commit?
  const diffCheck = await runGit(["diff", "--cached", "--quiet"], repoDir);
  if (diffCheck.exitCode === 0) {
    return { committed: false, hash: null, message };
  }
  const commit = await runGit(["commit", "-m", message], repoDir);
  if (commit.exitCode !== 0) {
    throw new BackupGitError(
      "git_failed",
      "git commit failed: " + (commit.stderr || "unknown").slice(0, 400)
    );
  }
  const sha = await runGit(["rev-parse", "HEAD"], repoDir);
  const hash = sha.exitCode === 0 ? sha.stdout.trim() : null;
  return { committed: true, hash, message };
}

export interface PushResult {
  pushed: boolean;
  reason?: string;
}

/**
 * Push to `origin/main`. Only call when:
 *   - the user explicitly set `push:true` in the request body
 *   - the config has a non-null remote
 *   - the local repo's `origin` remote is already configured by the user
 *
 * No credential probing, no token storage, no API calls. If the local
 * remote isn't set up the push will fail and we return the redacted git
 * error - the user is expected to run `git remote add origin <url>` once.
 */
export async function pushBackup(repoDir: string): Promise<PushResult> {
  const remoteCheck = await runGit(["remote"], repoDir);
  const remotes = remoteCheck.stdout
    .split("\n")
    .map((s) => s.trim())
    .filter(Boolean);
  if (!remotes.includes("origin")) {
    return {
      pushed: false,
      reason:
        "origin remote not configured; run `git remote add origin <url>` in " +
        repoDir,
    };
  }
  const r = await runGit(["push", "origin", "HEAD"], repoDir);
  if (r.exitCode !== 0) {
    return {
      pushed: false,
      reason: "git push failed: " + (r.stderr || "unknown").slice(0, 400),
    };
  }
  return { pushed: true };
}

