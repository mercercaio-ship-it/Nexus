/**
 * The .gitignore the backup repo gets on first init. The list is
 * intentionally defensive: it excludes the obvious DB / log / secret
 * fingerprints even if the user later turns on `includeSessionsDb` or
 * the source dir somehow gains an unwanted file. The route layer + copy
 * filter also refuse those files independently.
 */
export const BACKUP_GITIGNORE = [
  "# CreativEdge backup .gitignore (Phase 5.6-A).",
  "# Generated by the backend; safe to edit but the backend will rewrite",
  "# the standard exclusions on every dry-run / run if this file is missing.",
  "",
  "# SQLite databases (sessions, fts shadow files, journals).",
  "sessions.db",
  "sessions.db-*",
  "*.db",
  "*.sqlite",
  "*.sqlite3",
  "",
  "# Logs.",
  "logs/",
  "*.log",
  "",
  "# Provider / runtime config (never committed).",
  "providers.json",
  "profile.json",
  "",
  "# Secrets and credentials (never committed).",
  ".env",
  ".env.*",
  "*.key",
  "*.pem",
  "",
].join("\n") + "\n";

export function buildBackupGitignore(): string {
  return BACKUP_GITIGNORE;
}

// ---------------------------------------------------------------------------
// Remote redaction
// ---------------------------------------------------------------------------

/**
 * Strip user:password from an https-style remote so we never echo a token
 * the user accidentally embedded in the URL. SSH-form remotes are returned
 * unchanged. Returns null when the input is null/empty.
 *
 *   https://user:tok@github.com/owner/repo.git
 *      -> https://***@github.com/owner/repo.git
 *   https://github.com/owner/repo.git -> unchanged
 *   git@github.com:owner/repo.git    -> unchanged
 */
export function redactRemote(remote: string | null | undefined): string | null {
  if (!remote || typeof remote !== "string") return null;
  return remote.replace(/^(https?:\/\/)[^/@\s]+@/, "$1***@");
}

// ---------------------------------------------------------------------------
// Git availability + low-level wrapper
// ---------------------------------------------------------------------------

export interface GitInvokeResult {
  stdout: string;
  stderr: string;
  exitCode: number;
}

async function runGit(args: string[], cwd: string): Promise<GitInvokeResult> {
  try {
    const r = await execFileP("git", args, {
      cwd,
      timeout: GIT_TIMEOUT_MS,
      maxBuffer: MAX_OUTPUT_BYTES,
      shell: false,
      windowsHide: true,
    });
    return {
      stdout: String(r.stdout ?? ""),
      stderr: String(r.stderr ?? ""),
      exitCode: 0,
    };
  } catch (err) {
    // execFile rejects on non-zero exit OR on spawn failure (ENOENT when
    // git isn't installed). Re-route both to typed errors so the caller
    // can give a useful HTTP response.
    const e = err as NodeJS.ErrnoException & {
      stdout?: unknown;
      stderr?: unknown;
      code?: string | number;
    };
    if (e.code === "ENOENT") {
      throw new BackupGitError("git_missing", "git is not installed or not on PATH");
    }
    const stdout =
      typeof e.stdout === "string"
        ? e.stdout
        : Buffer.isBuffer(e.stdout)
          ? e.stdout.toString("utf-8")
          : "";
    const stderr =
      typeof e.stderr === "string"
        ? e.stderr
        : Buffer.isBuffer(e.stderr)
          ? e.stderr.toString("utf-8")
          : "";
    const exitCode = typeof e.code === "number" ? e.code : -1;
    return { stdout, stderr, exitCode };
  }
}

export async function gitAvailable(): Promise<boolean> {
  try {
    const r = await runGit(["--version"], process.cwd());
    return r.exitCode === 0 && /^git version /.test(r.stdout);
  } catch {
    return false;
  }
}

