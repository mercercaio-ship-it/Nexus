export async function ensureBackupRepo(repoDir: string): Promise<{ created: boolean }> {
  await mkdir(repoDir, { recursive: true });
  // Is this already a git repo?
  const gitDir = join(repoDir, ".git");
  let alreadyRepo = false;
  try {
    const s = await stat(gitDir);
    alreadyRepo = s.isDirectory();
  } catch {
    alreadyRepo = false;
  }
  if (alreadyRepo) return { created: false };

  const r = await runGit(["init", "--initial-branch=main"], repoDir);
  if (r.exitCode !== 0) {
    // Older git installs don't support --initial-branch; retry without it.
    const r2 = await runGit(["init"], repoDir);
    if (r2.exitCode !== 0) {
      throw new BackupGitError(
        "git_failed",
        "git init failed: " + (r2.stderr || r.stderr || "unknown").slice(0, 400)
      );
    }
  }
  // Give the new repo a sensible identity so commits don't fail later. We
  // use repo-local config (not --global) so we don't touch user-wide state.
  await runGit(["config", "user.email", "creativedge-backup@localhost"], repoDir);
  await runGit(["config", "user.name", "CreativEdge Backup"], repoDir);
  return { created: true };
}

/**
 * Write or refresh the .gitignore in the backup repo. Idempotent: if the
 * file already exists and matches `BACKUP_GITIGNORE` byte-for-byte, skip
 * the write. Otherwise write to a `.tmp` and rename.
 */
export async function writeBackupGitignore(
  repoDir: string
): Promise<{ wrote: boolean }> {
  const path = join(repoDir, ".gitignore");
  let existing = "";
  try {
    existing = await readFile(path, "utf-8");
  } catch (err) {
    if ((err as NodeJS.ErrnoException)?.code !== "ENOENT") {
      throw new BackupGitError(
        "io_error",
        "failed to read .gitignore: " + (err as Error)?.message
      );
    }
  }
  if (existing === BACKUP_GITIGNORE) return { wrote: false };
  await mkdir(repoDir, { recursive: true });
  await writeFile(path, BACKUP_GITIGNORE, { encoding: "utf-8" });
  return { wrote: true };
}

// ---------------------------------------------------------------------------
// Selective copy: agents/<slug>/memory/*.md only
// ---------------------------------------------------------------------------

const MEMORY_FILE_RE = /\.md$/i;
const ALLOWED_MEMORY_BASENAMES = new Set([
  "core_memory.md",
  "episodic_memory.md",
  // Optional README the user may keep alongside; allowed because it can't
  // expose anything not already in the runtime source.
  "README.md",
  "readme.md",
]);

export interface CopyResult {
  filesConsidered: number;
  filesCopied: number;
  filesSkipped: string[];
}

/**
 * Selectively mirror `<sourceDir>/<slug>/memory/*.md` into
 * `<repoDir>/agents/<slug>/memory/<file>.md`. The repo's `agents/` prefix
 * mirrors the source layout under the runtime root so a future restore
 * tool can reverse the operation by copying back.
 *
 * Filter rules:
 *   - Only `*.md` files inside a `<slug>/memory/` directory are considered.
 *   - Basename allowlist: `core_memory.md`, `episodic_memory.md`,
 *     `README.md` / `readme.md`. Anything else is skipped (and reported
 *     in `filesSkipped`).
 *   - `*.lock` / `*.tmp` are skipped explicitly.
 *   - Files > 4 MB are skipped (they're almost certainly the wrong file).
 *   - The slug directory name must match SLUG_RE used elsewhere; otherwise
 *     skipped.
 *
 * Path-safety: every resolved destination path must stay inside `repoDir`.
 */
const SLUG_RE = /^[a-z][a-z0-9-]{1,40}$/;

export async function syncAgentsToBackupRepo(
  sourceDir: string,
  repoDir: string
): Promise<CopyResult> {
  const considered: string[] = [];
  const skipped: string[] = [];
  let copied = 0;

  let slugEntries: string[];
  try {
    slugEntries = await readdir(sourceDir);
  } catch (err) {
    if ((err as NodeJS.ErrnoException)?.code === "ENOENT") {
      return { filesConsidered: 0, filesCopied: 0, filesSkipped: [] };
    }
    throw new BackupGitError(
      "io_error",
      "failed to list agents source dir: " + (err as Error)?.message
    );
  }

  const resolvedRepo = resolve(repoDir);
  const repoPrefix = resolvedRepo.endsWith(sep) ? resolvedRepo : resolvedRepo + sep;

  for (const slug of slugEntries) {
    if (!SLUG_RE.test(slug)) {
      skipped.push(`<sourceDir>/${slug}  (slug regex)`);
      continue;
    }
    const memDir = join(sourceDir, slug, "memory");
    let memFiles: string[];
    try {
      memFiles = await readdir(memDir);
    } catch {
      // No memory dir for this slug yet; not an error.
      continue;
    }

    for (const name of memFiles) {
      considered.push(`${slug}/memory/${name}`);
      if (considered.length > MAX_FILES_PER_RUN) {
        throw new BackupGitError(
          "too_many_files",
          `more than ${MAX_FILES_PER_RUN} candidate files; aborting for safety`
        );
      }
      if (name.endsWith(".lock") || name.endsWith(".tmp")) {
        skipped.push(`${slug}/memory/${name}  (lock/tmp)`);
        continue;
      }
      if (!MEMORY_FILE_RE.test(name)) {
        skipped.push(`${slug}/memory/${name}  (non-md)`);
        continue;
      }
      if (!ALLOWED_MEMORY_BASENAMES.has(name)) {
        skipped.push(`${slug}/memory/${name}  (basename not allowlisted)`);
        continue;
      }
      const srcPath = join(memDir, name);
      let s;
      try {
        s = await stat(srcPath);
      } catch {
        skipped.push(`${slug}/memory/${name}  (stat failed)`);
        continue;
      }
      if (!s.isFile()) {
        skipped.push(`${slug}/memory/${name}  (not a regular file)`);
        continue;
      }
      if (s.size > MAX_FILE_BYTES) {
        skipped.push(`${slug}/memory/${name}  (>${MAX_FILE_BYTES} bytes)`);
        continue;
      }
      const destPath = join(resolvedRepo, "agents", slug, "memory", name);
      const resolvedDest = resolve(destPath);
      if (resolvedDest !== resolvedRepo && !resolvedDest.startsWith(repoPrefix)) {
        throw new BackupGitError(
          "path_escapes_repo",
          `destination path escapes backup repo: ${resolvedDest}`
        );
      }
      const buf = await readFile(srcPath);
      await mkdir(dirname(destPath), { recursive: true });
      await writeFile(destPath, buf);
      copied++;
    }
  }

  return {
    filesConsidered: considered.length,
    filesCopied: copied,
    filesSkipped: skipped,
  };
}

